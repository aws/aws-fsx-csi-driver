func newCreatVolumeResponse(fs *cloud.FileSystem) *csi.CreateVolumeResponse {
	return &csi.CreateVolumeResponse{
		Volume: &csi.Volume{
			Id:            fs.FileSystemId,
			CapacityBytes: util.GiBToBytes(fs.CapacityGiB),
			Attributes: map[string]string{
				"dnsname": fs.DnsName,
			},
		},
	}
}
